    private void bTambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bTambahActionPerformed
        // TODO add your handling code here:
        String nim = txtNIM.getText();
        String nama = txtNama.getText();
        String kelamin;
        if(rLaki.isSelected()){
            kelamin = "L";
        } else if(rPere.isSelected()){
            kelamin = "P";
        } else {
            kelamin = "";
        }
        String agama = cAgama.getSelectedItem().toString();
        String tempatLahir = txtTempatLahir.getText();
        DateFormat dateFormat = new SimpleDateFormat("yyyy-MM-dd");
        String tglLahir = dateFormat.format(txtTglLahir.getDate());
        String noHP = txtHP.getText();
        int angkatan = Integer.parseInt(cAngkatan.getSelectedItem().toString());
        String asalSekolah = txtAsal.getText();
        String prodi = cProdi.getSelectedItem().toString();
        String alamat = txtAlamat.getText();
        try {
            String sql = "INSERT INTO mahasiswa(nim,nama,jenis_kelamin,agama,tempat_lahir,tgl_lahir,prodi,alamat,no_hp,asal_sekolah,angkatan) VALUES(?,?,?,?,?,?,?,?,?,?,?)";
            java.sql.Connection conn = koneksi.configDB();
            java.sql.PreparedStatement pst = conn.prepareStatement(sql);
            pst.setString(1, nim);
            pst.setString(2, nama);
            pst.setString(3, kelamin);
            pst.setString(4, agama);
            pst.setString(5, tempatLahir);
            pst.setString(6, tglLahir);
            pst.setString(7, prodi);
            pst.setString(8, alamat);
            pst.setString(9, noHP);
            pst.setString(10, asalSekolah);
            pst.setInt(11, angkatan);
            pst.executeUpdate();
            JOptionPane.showMessageDialog(null, "Penyimpanan Berhasil");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
        load_table_mhs();
        kosongi();
        
    }//GEN-LAST:event_bTambahActionPerformed

