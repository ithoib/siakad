    private void jTMhsMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTMhsMouseClicked
        try {
            // TODO add your handling code here:
            int baris = jTMhs.rowAtPoint(evt.getPoint());
            txtNIM.setText(jTMhs.getValueAt(baris, 0).toString());
            txtNIM.setEditable(false);
            txtNama.setText(jTMhs.getValueAt(baris, 1).toString());
            cProdi.setSelectedItem(jTMhs.getValueAt(baris, 2).toString());
            String JK = jTMhs.getValueAt(baris, 3).toString();
            if(JK.equals("L")){
                rLaki.setSelected(true);
            } else {
                rPere.setSelected(true);
            }
            cAgama.setSelectedItem(jTMhs.getValueAt(baris, 4).toString());
            txtTempatLahir.setText(jTMhs.getValueAt(baris,5).toString());
            Date date = new SimpleDateFormat("yyyy-MM-dd").parse(jTMhs.getValueAt(baris, 6).toString());
            txtTglLahir.setDate(date);
            txtHP.setText(jTMhs.getValueAt(baris, 7).toString());
            cAngkatan.setSelectedItem(jTMhs.getValueAt(baris, 8).toString());
            txtAsal.setText(jTMhs.getValueAt(baris, 9).toString());
            txtAlamat.setText(jTMhs.getValueAt(baris, 10).toString());
        } catch (ParseException ex) {
            Logger.getLogger(mahasiswanew.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jTMhsMouseClicked

