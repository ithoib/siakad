    private String getFakID(String nama){
        try {
  
            String sql = "SELECT * FROM fakultas WHERE nama_fakultas='" + nama + "'";
            java.sql.Connection conn = koneksi.configDB();
            java.sql.Statement stm = conn.createStatement();
            java.sql.ResultSet res = stm.executeQuery(sql);
            while (res.next()) {
                return res.getString("id_fakultas");
            }
        } catch (Exception e) {
        }
        return "";
    }
    private void comboFakultas(){
        try {
  
            String sql = "SELECT * FROM fakultas";
            java.sql.Connection conn = koneksi.configDB();
            java.sql.Statement stm = conn.createStatement();
            java.sql.ResultSet res = stm.executeQuery(sql);
            while (res.next()) {
                cFakultas.addItem(res.getString("nama_fakultas"));
            }
        } catch (Exception e) {
        }
        
    
    }

