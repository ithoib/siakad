    private void load_table_prodi() {
        DefaultTableModel modelprod = new DefaultTableModel();
        modelprod.addColumn("Kode Prodi");
        modelprod.addColumn("Nama Prodi");
        modelprod.addColumn("Fakultas");
        try {
  
            String sql = "SELECT p.kode_prodi,p.nama_prodi,f.nama_fakultas FROM prodi p LEFT JOIN fakultas f ON p.fakultas=f.id_fakultas";
            java.sql.Connection conn = koneksi.configDB();
            java.sql.Statement stm = conn.createStatement();
            java.sql.ResultSet res = stm.executeQuery(sql);
            while (res.next()) {
                modelprod.addRow(new Object[]{res.getString(1), res.getString(2), res.getString(3)});
            }
            jTProdi.setModel(modelprod);
        } catch (Exception e) {
        }

    }

